   <h3>Dean Electronics - Dean Moorehouse</h3>
   <div id="image1about"><img src="images/DeanAtWIT.jpg" alt="Dean with robotic blimp at W.I.T." width="415" height="371" align="left" /></div>
   
   <div id="deaninforight"><p>Dean Electronics is a small business set up by Dean Moorehouse, which specialises in building and modifying guitar effects pedals, and tube amplifiers.</p>
<p>Dean was born in Wexford in 1987. While in his mid teens, and still in secondary school, Dean was very much enthralled in music, in particular classic and hard rock, from legends such as Led Zepellin, to the energetic Guns N' Roses, and also metal bands such as Iron Maiden and Megadeth. As he sought out as many great artists as he could, he discovered virtuosos like Marty Friedman and Jason Becker. All this musical influence fueled Deans desire to take up guitar, which, as was inevitable, he did! He had been dedicated to his guitars ever since! Spending as much time as he possibly could playing guitar, it didn't take long for Dean to start becoming proficient.</p></div>

<div id="deaninfomain">
<p>After finishing the Leaving Certificate, Dean went on to study Electronic Engineering for 5 years at Waterford Institute of Technology. There he learned a great deal about electronics. As Dean's knowledge and experience of electronic engineering grew, he soon made the connection between the equipment he used to play the instrument he loved, and the practical skills he was honing in college.</p>

<p>Dean became as enthusiastic in his study of how his amplifiers and effects pedals worked, as was in playing them. He quickly began to understand at a fundamental level, aided by his training in college, the workings of his equipment, and what the electronic components inside were for, what they did and how they did it.</p>
</div>

<div id="deaninfoleft">
<p>Dean soon began to see how to apply this knowledge. Spending much time researching, Dean eventually gathered enough knowledge to begin designing his very first homemade, custom designed amplifier. Purchasing all the pieces online, Dean took a jumble of electronic components put them together to produce an amplifier, built at home in his bedroom.</p>
<p>The amp used electronic valves, which are known to produce nicer, warmer tones than the typical solid state amp you might buy around town. After hours of careful work on the amplifier, Dean was finally ready to give a test run.</p>
</div>

<div id="image2about">
  <img src="images/deanamps.jpg" width="400" height="300" alt="two home made guitar amps" /></div>
  
<div id="deaninfomain2">
<p>The tone was fantastic! The result exceeded even his own expectations, producing a sound that was clear and bright, warm and nicely overdriven. Dean continued to learn more, and built many more homemade amplifiers, and took the time to build a few effects pedals too! He once offered to modify his brothers Metal Zone 2 effects pedal, which his brother took him up on. Dean modified the pedal, even adding in an LED light that lit up when it received signal from the guitar. Dean offered to do the mod as a favour to his brother, who wasnt satisfied with the pedals sound after he bought it, and also as a learning experience. As such, he did the modifications free of charge.</p>

<p>When Dean's brother tried out the effects pedal with the new modifications, he was so pleased that he insisted on paying for the mod, even though it had been done for him for free! He appreiated the improvement in tone so much that he would not allow Dean to not take payment for the pedal mod!</p>

<p>Dean has since finished his degree at WIT, and now works as an RF Design Engineer, at a cork based electronics company called Socowave. He has recently been to Korea to work for a number of weeks at Samsung Digital City. As he continues a successful career in electronics, Dean is continually building guitar equipment, designing new amplifiers and pedals based on the skills and experience he has picked up over the years, making ever more ambitious designs, knowing that no matter what he achieves, he will always keep learning and researching so that he can keep designing ever more improved amps and effects pedals.</p>
</div>

